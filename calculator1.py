what = input("what we will do (+, -, *, /, **, %) : ")
a = int(input("first number : "))
b = int(input("second number : "))
if what == "+":
	c = a + b
	print(c)
if what == "-":
	c = a - b
	print(c)
if what == "*":
	c = a * b
	print(c)
if what == "/":
	c = a / b
	print(c)
if what == "**":
	c = a ** b
	print(c)
if what == "%":
	c = a % b
	print(c)
